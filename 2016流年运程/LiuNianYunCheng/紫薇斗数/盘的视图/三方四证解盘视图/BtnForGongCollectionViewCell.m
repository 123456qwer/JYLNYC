#import "BtnForGongCollectionViewCell.h"

@implementation BtnForGongCollectionViewCell

- (instancetype)initWithFrame:(CGRect)frame
{
    
    if (self == [super initWithFrame:frame]) {
        
        _btnForGong = [[UILabel alloc]initWithFrame:CGRectMake(0, 0, self.width, self.height)];
        _btnForGong.numberOfLines = 0;
        _btnForGong.textAlignment = NSTextAlignmentCenter;
        _btnForGong.font = [UIFont boldSystemFontOfSize:19];
        _btnForGong.textColor = [UIColor whiteColor];
        [self addSubview:_btnForGong];
        
    }
    
    return self;
}

@end
